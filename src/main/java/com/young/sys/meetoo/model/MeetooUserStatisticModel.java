package com.young.sys.meetoo.model;

public class MeetooUserStatisticModel {

    private Integer momentsCount;

    private Integer fansCount;

    private Integer followsCount;

    public Integer getMomentsCount() {
        return momentsCount;
    }

    public void setMomentsCount(Integer momentsCount) {
        this.momentsCount = momentsCount;
    }

    public Integer getFansCount() {
        return fansCount;
    }

    public void setFansCount(Integer fansCount) {
        this.fansCount = fansCount;
    }

    public Integer getFollowsCount() {
        return followsCount;
    }

    public void setFollowsCount(Integer followsCount) {
        this.followsCount = followsCount;
    }
}
